class Solution
{
    // The given root is the root of the Binary Tree
    // Return the root of the generated BST
    static Node bstRoot ;
    Node binaryTreeToBST(Node root)
    {
       // Your code here
       bstRoot = null;
       ArrayList<Integer> arr = new ArrayList<>();
       fillTheArrayWithTreeNodes(root, arr);
       Collections.sort(arr);
       makeBst(arr, 0, arr.size()-1);
       return bstRoot;
       
    }
    static void  fillTheArrayWithTreeNodes(Node root, ArrayList<Integer> arr ){
        if(root==null){
            return;
        }
        arr.add(root.data);
        fillTheArrayWithTreeNodes(root.left, arr);
        fillTheArrayWithTreeNodes(root.right, arr);
    }
    static Node makeBst(ArrayList<Integer> arr,int  start, int end){
        if(start>end){
            return null;
        }
        int mid = (start+end)/2;
        int k = arr.get(mid);
        Node temp ;
        if(bstRoot == null){
            bstRoot = new Node(k);
            temp = bstRoot;
        }
        else{
            temp = new Node(k);
        }
        temp.left = makeBst(arr,start,mid-1);
        temp.right = makeBst(arr,mid+1,end);
        return temp;
    }
    
}
 